package sh.pcx.unified.visual.bossbar;

/**
 * Overlay styles for boss bars.
 *
 * @since 1.0.0
 * @author Supatuck
 */
public enum BossBarOverlay {

    /**
     * Solid progress bar with no divisions.
     */
    PROGRESS,

    /**
     * Bar divided into 6 segments.
     */
    NOTCHED_6,

    /**
     * Bar divided into 10 segments.
     */
    NOTCHED_10,

    /**
     * Bar divided into 12 segments.
     */
    NOTCHED_12,

    /**
     * Bar divided into 20 segments.
     */
    NOTCHED_20
}
